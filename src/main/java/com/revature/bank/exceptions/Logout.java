package com.revature.bank.exceptions;

public class Logout extends ReturnMainMenu {
	private static final long serialVersionUID = 3272846475159072662L;
	public Logout() {
		super();
	}
	public Logout(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
	public Logout(String message, Throwable cause) {
		super(message, cause);
	}
	public Logout(String message) {
		super(message);
	}
	public Logout(Throwable cause) {
		super(cause);
	}
}
